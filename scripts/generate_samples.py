def generate(rows: list[tuple[str, int]], path: str) -> None:
    df = pd.DataFrame(rows, columns=["text", "label"])
    os.makedirs(OUT_DIR, exist_ok=True)
    df.to_csv(path, index=False, encoding="utf-8-sig")

    counts = df["label"].value_counts().sort_index()
    label_names = {0: "부정", 1: "중립", 2: "긍정"}
    print(f"  저장: {path}  ({len(df)}건)")
    for lbl, cnt in counts.items():
        print(f"    {label_names[lbl]}({lbl}): {cnt}건")


def main() -> None:
    print("[generate_samples] 샘플 데이터 생성 시작")

    review_path = f"{OUT_DIR}/synthetic_reviews.csv"
    chat_path   = f"{OUT_DIR}/synthetic_chats.csv"

    print("\n[1] 쇼핑몰 리뷰 데이터")
    generate(REVIEW_ROWS, review_path)

    print("\n[2] 고객센터 채팅 메시지")
    generate(CHAT_ROWS, chat_path)

    total = len(REVIEW_ROWS) + len(CHAT_ROWS)
    print(f"\n완료. 총 {total}건 생성됨.")
    print(f"다음 단계:")
    print(f"  poetry run python scripts/preprocess.py --source local --csv_path {review_path}")


if __name__ == "__main__":
    main()
